async function main() {
  console.log(`Начинаем обновление seoH1 для ${Object.keys(SEO_H1_MAP).length} товаров...\n`);

  let updated = 0;
  let notFound = 0;
  const notFoundSlugs: string[] = [];

  for (const [slug, seoH1] of Object.entries(SEO_H1_MAP)) {
    const product = await prisma.product.findUnique({ where: { slug } });

    if (!product) {
      notFound++;
      notFoundSlugs.push(slug);
      continue;
    }

    await prisma.product.update({
      where: { slug },
      data: { seoH1 },
    });

    console.log(`✓ ${slug} → "${seoH1}"`);
    updated++;
  }

  console.log(`\n========================================`);
  console.log(`Обновлено: ${updated}`);
  console.log(`Не найдено в БД: ${notFound}`);

  if (notFoundSlugs.length > 0) {
    console.log(`\nСлуги не найденных товаров (проверьте slug в БД):`);
    notFoundSlugs.forEach((s) => console.log(`  - ${s}`));
  }

  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  prisma.$disconnect();
  process.exit(1);
});
